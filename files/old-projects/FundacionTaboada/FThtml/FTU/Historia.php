				<span class="title">Acerca de la fundación</span>
				<div class="cont">
					<p>El 4 de agosto de 2014 la Fundación Protectora de Puebla "Aurora Marín de Taboada" cumplirá 35 años de haber otorgado sus dos primeras becas para estudiantes universitarios a nivel Licenciatura.</p>
					<span class="subtitle">¿Pero qué es la Fundación Protectora de Puebla "Aurora Marín de Taboada"?</span>
					<p>La Fundación Protectora de Puebla “Aurora Marín de Taboada” es una institución legalmente constituida mediante el Instrumento 4930 en la Ciudad de Puebla en la Notaria No 11 del Lic. José Asomoza con fecha 13 de Junio de 1978. Está reconocida por la Junta para el Cuidado de las Instituciones de Beneficencia Privada del Estado de Puebla, y también se encuentra registrada ante la Secretaría de Hacienda y Crédito Público.</p>
					<p>Fue establecida por Don Domingo Taboada Roldán, quien se interesó especialmente por brindar oportunidades de adquirir Educación Formal a jóvenes que mostraran una comprobada disposición hacia el estudio.</p>
					<p>La Fundación Taboada  se maneja de acuerdo al ideario y objetivos creados por Don Domingo Taboada, que son acordes a la cultura e identidad del país. De este modo la Fundación Taboada se caracteriza por ser respetuosa de las ideas de los demás. Es por esta razón que da libertad de conciencia, creencia religiosa, filosófica y política a los Becarios, pero les pide que respeten los principios y metas de la Institución, así como las normas de la moral y del derecho, y que no hagan proselitismo de sus ideas.</p>
					<p>La Fundación Taboada, está compuesta por un Patronato que la administra : Un Presidente, Dn. Rafael Taboada Marín, quien ocupó el cargo de 1979 a 1994 y que desde ese año ha sido sustituido por la LAE. Ma. Del Socorro Elizabeth Taboada Avilés.</p>
					<ul>
						<li>Un tesorero: Q.F.B. Hermelinda Taboada Ortiz</li>
						<li>Un Secretario: Sr. Raúl Angulo Pérez</li>
						<li>Dos vocales: Sr. Alfonso Hernández García y el Sr. Luis Rojano Villegas.</li>
					</ul>
					<p>La Institución tiene dos actividades definidas, pero diferentes: Una es la parte Administrativa que maneja su Patrimonio a través de la Renta de Bienes Inmuebles. Funciona como una empresa normal que posee una Cultura Organizacional, hace Planeación Estratégica y tiene Auditorías de Desempeño, de modo que pueda servir a sus clientes con excelencia y calidad.</p>
					<p>La segunda actividad de la Fundación es la que le da sentido a su existencia: La Educación. Cuenta con un Departamento en el cual hay una área de Admisiones que se encarga de seleccionar a los aspirantes a Beca mediante la aplicación de exámenes Académicos y Psicométricos haciendo además un estudio socioeconómico de manera que la Beca sea otorgada realmente a jóvenes con mérito y disposición académica y con una necesidad económica de ella.</p>
					<p>La Fundación Taboada hace también un seguimiento personal de cada becario a lo largo de sus estudios, manteniendo un contacto continuo para apoyarlos en los posibles problemas que se les presenten.</p>
					<p>También la Fundación apoya algunas carreras técnicas como Mecánica Automotriz, Enfermería y Carpintería.</p>
					<p>En la actualidad la Fundación mantiene en proceso de estudios a 75 Becarios en instituciones como la UDLA, el ITESM, la UPAEP, la ULSA, el ITAM, en sus diversos Campus. Al día de hoy  la Fundación Taboada ha egresado a 536 Jóvenes titulados tanto de Licenciaturas Universitarias como de Carreras Técnicas.</p>
					<p>La Fundación Taboada desde el año 2000 implementó el Programa "Educar para un México Mejor" que consiste en la impartición de clases a estudiantes de Primaria, Secundaria y Preparatoria que tienen problemas de estudio, en especial con las materias de Matemáticas, Física, Química e Inglés. También reciben una clase de Valores Universales. Estas materias son impartidas por los Becarios de la Fundación quienes realizan su Servicio Social en este programa. Las clases se dan en el mes de Julio de cada año y los grupos tienen un máximo de 6 alumnos, de manera que se les enseñan estás materias en forma personalizada resolviendo sus dudas y aplicando un gran número de ejercicios que les permitan a los niños y adolescentes  desarrollar su habilidad en dichos temas.</p>
					<p>Estos cursos son totalmente gratuitos y de esta forma se ayuda a los Padres de Familia que no tienen recursos económicos para pagar las clases particulares de regularización de sus hijos, estos alumnos pueden provenir de cualquier colegio público o privado.</p>
					<p>Por el buen manejo de la Fundación a través de todos estos años, ha tenido la oportunidad de tener el apoyo de Instituciones de reconocido prestigio como el Nacional Monte de Piedad, Volkswagen de México, y de Money Gram Internacional, con los cuales se ha compartido en algún momento algún programa Educativo. También la Institución ha sido reconocida por la JIB, la UDLA, la UPAEP, La Fundación Comunitaria, por la Fundación Una Nueva Esperanza, Instituto Profesional Panamericano A.C., el Instituto de Desarrollo Integral y el IPETH.</p>
					<p>La Fundación a través de estos 34 años siempre ha estado en mejora continua y generando nuevos proyectos para apoyar la Educación.</p>
