				<span class="title">About the foundation</span>
				<div class="cont">
					<p>On August 4, 2014, Fundación Protectora de Puebla "Aurora Marín de Taboada" will reach 35 years of having granted its two first scholarships for bachelor university students.</p>
					<span class="subtitle">But what is Fundación Protectora de Puebla "Aurora Marín de Taboada"?</span>
					<p>Fundación Protectora de Puebla “Aurora Marín de Taboada” is an institution legally constituted by means of the Instrument 4930, dated June 13, 1978, in the Cityof Puebla in the Notary’s Office No. 11 of José Asomoz. It is acknowledged by the Board for the Citizen of the Private Charitable Institutions of the Stateof Puebla, and it is also registered before the Ministry of Finance and Public Credit.</p>
					<p>It was established by Mr. Domingo Taboada Roldán, who was especially interested in providing opportunities to acquire Formal Education to young people who showed a proven willingness to study.</p>
					<p>The Taboada Foundation is managed according to the ideology and objectives created by Mr. Domingo Taboada, which are in conformity with the country’s culture and identity. In this way, the Taboada Foundation is characterized by being respectful to the ideas of others. This is the reason why it gives freedom of conscience, and of religious,  and political believes to the grantees, but asks them to respect the principles and goals of the Institution, as well as the moral and legal standards, and not to proselytize their ideas.</p>
					<p>The Taboada Foundation is composed by a Patronage that manages it: A President, Rafael Taboada Marín, who occupied the position from 1979 to 1994 and that has been substituted since then by the BBA María Del Socorro Elizabeth Taboada Avilés.</p>
					<ul>
						<li>A Treasurer: Q.F.B. HermelindaTaboada Ortiz</li>
						<li>A Secretary: Mr. RaúlAngulo Pérez</li>
						<li>Two members: Mr. Alfonso Hernández García andMr. Luis Rojano Villegas.</li>
					</ul>
					<p>The Institution has two defined but different activities: One is the Administrative part that handles its Patrimony through the Real State Income. It works as a normal company, with an Organizational Culture; it performs Strategic Planning and has Performance Audits so that it can serve its clients with excellence and quality.</p>
					<p>The second activity of the Foundation is the one that gives meaning to its existence: Education. It counts with a Department with an Admission area in charge of selecting the applicants for a Scholarship by means of the application of Academic and Psychometric Tests and of performing, additionally, a socioeconomic study in order for the Scholarship to really be granted to young people with merit and academic willingness, and with an economic need of it.</p>
					<p>The Taboada Foundation also undertakes a personal tracking of every grantee during their studies, maintaining continuous contact to support them in the possible problems that may arise.</p>
					<p>In addition, the foundation supports some technical careers such as Automotive Mechanics, Nursing and Carpentry.</p>
					<p>Currently, the Foundation sustains 75 grantees in process of studies in institutions like UDLA, ITESM, UPAEP, ULSA and ITAM, in their different Campuses. To date, the Taboada Foundation has graduated 536 young students from both University Bachelors and Technical Careers.</p>
					<p>Since the year 2000, the Taboada Foundation implemented the Program "Educating for a better Mexico" that consists in teaching classes to Elementary, Junior High and High school students with study problems, especially with the subjects of Mathematics, Physics, Chemistry and English. They also receive a class of Universal Values. These subjects are taught by the Foundation Grantees, which do their social service in this program. The classes are given in the month of July each year and the groups have a maximum capacity of six students, so that these subjects are taught in a personal manner, solving each doubt and applying a great number of exercises that allow the kids and teens to develop their ability in said subjects.</p>
					<p>These courses are totally free of charge and the Parents that lack the economic resources to pay for private remedial classes for their children are being helped in this way. These students may come from any school, be it public or private.</p>
					<p>Due to the good management of the Foundation overall these years, it has had the opportunity of counting with the support of institutions of renowned prestige such as Nacional Monte de Piedad, Volkswagen Mexico and Money Gram International, with which it has shared at some point an Educational Program. The Institution has also been acknowledged by JIB, UDLA, UPAEP, the Community Foundation, the New Hope Foundation, Instituto Profesional Panamericano A.C., the Institute for Integral Development and PETH.</p>
					<p>Through all these years the Foundation has always been in continuous improvement and generating new projects to support the Education.</p>
