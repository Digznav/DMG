						<h3>Requirements</h3>
						<div>
							<ul>
								<li>6 recent photographs size mignon.</li>
								<li>A photocopy of the high school certificate (a grades certificate can be delivered provisionally).</li>
								<li>A photocopy of the junior high certificate.</li>
								<li>A letter addressed to the Foundation, stating the reasons why the scholarship is being requested.</li>
								<li>Two academic recommendation letters.</li>
								<li>A photocopy of the birth certificate.</li>
								<li>A health certificate.</li>
								<li>A thoracic-pulmonary file of the Health Department (S.S.A)</li>
								<li>V.D.R.L. from the UPAEP Hospital.</li>
								<li>Proof of Family Income.</li>
								<li>A photocopy of the last paid phone bill or rent receipt.</li>
							</ul>
							<p><strong>NOTE:</strong> <em>These documents must be delivered alongside the scholarship application that is provided in the offices of this Foundation at the stipulated date.</em></p>
						</div>
						<h3>Conditions to keep the scholarship</h3>
						<div>
							<ol>
								<li>Be a regular student.</li>
								<li>Maintain an average of nine in each semester of the major.</li>
								<li>Not marrying in the entire major. In case of so doing and dropping the major, he or she shall reimburse the totality of what was granted.</li>
								<li>Perform the Social Serviceof 480 hours in the programs of the Foundation.</li>
								<li>In case of disease, notify the Foundation and the University immediately.</li>
								<li>The grantee may not work during the Major.</li>
							</ol>
							<p>The data contained in the information provided by the applicants is confidential and shall be verified, so the applicant is committed regarding its strict veracity, in such a way that any omission or inaccuracy will be reason enough not to deem valid the application for this Foundation.</p>
							<p><strong>NOTE:</strong> <em>The student that without valid justification drops the studies, shall be obliged to pay the amount of the tuitions that the Foundation had covered until then.</em></p>
