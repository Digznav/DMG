						<h3>Applicant profile</h3>
						<div>
							<ul>
								<li>Be of Mexican nationality.</li>
								<li>Have finished <strong>high school completely and satisfactory</strong> at any <strong>school or vocational</strong> of the country, with a total <strong>average of nine</strong>.</li>
								<li>That the current economic situation of the applicant deserves the granting of the scholarship.</li>
								<li>Successfully passing, according to the Education Department of the Foundation, the knowledge exam applied by the Foundation.</li>
								<li>Taking the psychological tests required by this institution.</li>
								<li>Passing the admission test of the University or Higher Education Institution in which he or she wishes to enter, as well as meeting the admission requirements of the latter.</li>
								<li>Not being married.</li>
								<li>Delivering the documentation requested by this Foundation.</li>
								<li>Following all the procedures personally.</li>
							</ul>
						</div>
						<h3>Benefices</h3>
						<div>
							<p>The Scholarships Department provides the opportunity of studying a Bachelor in the following Universities of the Country:</p>
							<ul>
								<li>Anahuac University. *</li>
								<li>Autonomous Popular University of the State of Puebla (UPAEP)</li>
								<li>Ibero-american University Campus Golfo Centro.</li>
								<li>Monterrey Institute of Technology and Higher Education (any campus). *</li>
								<li>Fundación Universidad de las Américas de Puebla. *</li>
								<li>ITAM. *</li>
							</ul>
							<small>* Special conditions to consult with the Patronage.</small>
