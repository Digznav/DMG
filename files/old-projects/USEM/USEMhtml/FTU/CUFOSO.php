							<p>El Curso de Formación Social, CUFOSO, inicia actividades con <strong>nueva didáctica, estructura y contenidos.</strong><br>Fue concebido para los líderes que día a día se enfrentan ante la difícil tarea de <strong>tomar decisiones</strong> humanamente dignas, justas y responsables que conduzcan al <strong>crecimiento y la productividad.</strong><br>A través de este programa la USEM forma la conciencia social de cientos de mujeres y hombres de negocios en México.</p>
							<br>
							<p><span class="underline">Destinatarios:</span> Empresarios y dirigentes de empresa u organizaciones sociales.</p>
							<p><span class="underline">Objetivo General:</span></p>
							<ul>
								<li>Que el participante evalúe y modifique las acciones relacionadas con su desarrollo integral y el de su empresa, a partir de un marco de Principios Sociales Universales (PSU), con el fin de generar estrategias para el ejercicio de la responsabilidad social que transforme a la organización en un ámbito de relaciones significativas, buscando siempre altos niveles de rentabilidad y eficacia en las condiciones del entorno.</li>
							</ul>
							<p><span class="underline">Objetivos Específicos:</span></p>
							<ul>
								<li>Comprender los principios sociales universales para integrarlos a su vida familiar, laboral y social con una visión integral.</li>
								<li>Construir un modelo propio de gestión directiva basado en los principios del Pensamiento Social Cristiano (PSC) para reconocer la relevancia y el impacto de posicionar a la persona como centro y fin de toda actividad económica.</li>
								<li>Examinar la realidad cambiante del entorno bajo la óptica del Pensamiento Social Cristiano, y fortalecer así una visión integral empresa-sociedad para que el dirigente asuma un liderazgo social</li>
							</ul>
