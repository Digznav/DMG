        	<span class="tituloPormo">Cuetzalan</span><br /><br />
        	El nombre de Cuetzalan proviene de los vocablos en nahuatl cuezali, que significa "pájaro colorado" y la preposición –tlán-  o  -lan-  que significa lugar. En este sentido, Cuetzalan puede definirse como el "lugar donde abundan los pájaros colorados".<br /><br />
          Algunos autores definen a Cuetzalan como el "lugar donde abundan los quetzales", sin embargo, es importante marcar la diferencia entre un cuezali y un quetzalli (Simeón 1999:426). Si bien los dos son especies de aves de plumas preciosas; el primero se trata de un pájaro de plumas rojas y el segundo, es un pájaro de plumas largas y verdes.<br /><br />
          Cuetzalan se encuentra en la Sierra Norte de Puebla y su altitud es de 1000 metros sobre el nivel del mar.Presenta las siguientes coordenadas geográficas: 20° 01' de latitud norte y 97° 31' de longitud oeste.<br /><br />
          Es un pueblo que refleja la arraigada tradición y costumbre de los antepasados indígenas, por ello en el año 2002, fue declarado pueblo mágico por la Secretaria de Turismo Federal, por conservar toda una forma de vida autentica: arquitectura vernácula, fiestas yatractivos naturales y culturales.<br /><br />
          La arquitectura del pueblo es de calles empedradas, casas de piedra, techos de teja a doble agua y madera.<br /><br />
          Dentro de las principales fiestas del pueblo están la feria del café y del huipil y se llevan a cabo el 4 de octubre, fecha en que se celebra al santo patrón San Francisco de Asís.<br /><br />
          Los principales atractivos naturales son cascadas, ríos, pozas para nadar y toda una diversidad de flora en la que destacan: helechos arborescentes, heliconeas y las bellísimas orquídeas silvestres.<br /><br />
          En cuanto a atractivos culturales está la zona arqueológica de Yohualichan que en náhuatl significa casa de la noche y sus primeros habitantes fueron totonacos,debido a esto, Cuetzalan se considera dentro del totonacapan poblano.<br /><br />
          Otra parte importante del quehacer cotidiano de los pobladores es la gran variedad de productos artesanales, dentro de los que hay que destacar los textiles en telar de cintura y los bordados a mano:
          <ul>
          	<li>Telar de cintura como: huipiles, rebozos, blusas, bufandas, vestidos, diademas morrales y otras prendas como los rebozos de seda, y el juego de mantelitos individuales y las servilletas.</li>
            <li>Bordados a mano: blusas tradicionales, blusas de manga larga, vestidos y juegos de mantelitos.</li>
          </ul>
          La cestería es otra actividad artesanal, en ella se utilizan fibras naturales, como el jonote y el bejuco, con estos materiales se fabrican huacales,  canastas, cestos para basura, hueveras, maceteros redondos, morrales y paneros.<br /><br />
          El taller de cerería también es muy importante, en el se elaboran velas de cera de abeja para consumo doméstico, además de las ceras de lujo. Estas velas de cera y parafina que pueden ser de gran tamaño, con flores,  hojas y distintas figuras, siempre son regaladas por el mayordomo el día de la fiesta de cada uno de los santos que se festejan.<br /><br />
          <span class="tituloPormo">Danzas.</span><br /><br />
          La danza en la época prehispánica era una manifestación cultural que permitía cohesionar a los grupos indígenas que la realizaban porque unía en el momento de llevar al cabo el acto, la construcción del sentido del universo, de la vida, del entorno y la relación con los dioses. La danza prehispánica era una acción que expresaba en su organización, preparación y realización, la manera de entender la importancia de la naturaleza, de los animales y de la existencia humana en constante regeneración.<br /><br />
          Dentro de las principales danzas tenemos a la danza de los voladores, los quetzales o cuetzaltines y los negritos.<br /><br />
          Otroaspecto importante que no puede faltar es la exquisita gastronomía, y para el caso de Cuetzalan tenemos la tradicional cecina ahumada que se acompaña con deliciosostlayoyos o enchiladas, la sopa de hongos,  las acamayas cocinadas al mojo de ajo y que se pescan en los ríos de la región. Y para acompañar estos deliciosos platillos debemos de tomar una buena taza de café, agua de maracuyá o bien un licor de yolixpan.
Podríamos seguir hablando más y más acerca de este hermoso pueblo, sin embargo, mejor ven y Descubre Cuetzalan, porque estamos seguros de que vivirás una….. "mágica experiencia". 
